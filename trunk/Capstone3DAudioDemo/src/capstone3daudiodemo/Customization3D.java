    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String HRTFloc1 = "/capstone3audiodemo/resources/"+subjects[currentHRTF]+"_1.wav";
        String HRTFloc2 = "/capstone3daudiodemo/resources/"+subjects[currentHRTF]+"_2.wav";
        String HRTFloc3 = "/capstone3daudiodemo/resources/"+subjects[currentHRTF]+"_3.wav";
        String HRTFloc4 = "/capstone3daudiodemo/resources/"+subjects[currentHRTF]+"_4.wav";

        File soundFile1 = new File(getClass().getResource(HRTFloc1).getFile());
        File soundFile2 = new File(getClass().getResource(HRTFloc2).getFile());
        File soundFile3 = new File(getClass().getResource(HRTFloc3).getFile());
        File soundFile4 = new File(getClass().getResource(HRTFloc4).getFile());

        playSound(jLabel1, soundFile2, c1, m1);
        playSound(jLabel2, soundFile1, c2, m2);
        playSound(jLabel3, soundFile3, c3, m3);
        playSound(jLabel4, soundFile4, c4, m4);


        //Now that all of the sounds have been played
        //Update the mode and change which buttons are visible
        if(mode == 0)
        {
            mode = 1;
            //Reveal great, medium, bad buttons
            jButton2.setEnabled(true);
            jButton3.setEnabled(true);
            jButton4.setEnabled(true);
            //Update message to tell user to press button
            jLabel5.setText("Please evaluate how well the sound matched the visual indicators.");
        }
        if(mode == 2)
        {
            mode = 3;
            //Reveal better, worse buttons
            jButton5.setEnabled(true);
            jButton6.setEnabled(true);
            //Update message to tell user to press button
            jLabel5.setText("Please indicate whether the sound matched the indicators better or worse than the previous sound.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

