    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        //Better button
        currentHRTF += (direction*range);
        if(currentHRTF<0)
            currentHRTF=0;
        if(currentHRTF>=numHRTFs)
            currentHRTF = numHRTFs-1;
        range = range/2;
        if(range>0)
        {
            mode = 2;
            //Hide better/worse buttons
            jButton5.setEnabled(false);
            jButton6.setEnabled(false);
            jLabel5.setText("A new 3D sound sample has been prepared.  Press Test Sound to hear the sample.");
        }
        else
        {
            mode = 4;
            //Hide all buttons
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton4.setEnabled(false);
            jButton5.setEnabled(false);
            jButton6.setEnabled(false);
            jLabel5.setText("Your 3D audio has been prepared.  Press OK to save settings.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        //Worse button
        direction *= -1;
        if(currentHRTF == numHRTFs-1 && range>0)
            direction = -1;
        if(currentHRTF==1 && range>0)
            direction = 1;
        currentHRTF += (direction*range);
        if(currentHRTF<0)
            currentHRTF=0;
        if(currentHRTF>=numHRTFs)
            currentHRTF = numHRTFs-1;
        range = range/2;
        if(range>0)
        {
            mode = 2;
            //Hide better/worse buttons
            jButton5.setEnabled(false);
            jButton6.setEnabled(false);
            jLabel5.setText("A new 3D sound sample has been prepared.  Press Test Sound to hear the sample.");
        }
        else
        {
            mode = 4;
            //Hide all buttons
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton4.setEnabled(false);
            jButton5.setEnabled(false);
            jButton6.setEnabled(false);
            jLabel5.setText("Your 3D audio has been prepared.  Press OK to save settings.");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void playSound(javax.swing.JLabel label, File soundfile, javax.swing.ImageIcon color, javax.swing.ImageIcon mono)
    {
        AudioInputStream sound = null;
        try{sound = AudioSystem.getAudioInputStream(soundfile);}
        catch(Exception e) {System.out.println("Failed to load sound file");}

        // load the sound into memory (a Clip)
        DataLine.Info info = new DataLine.Info(Clip.class, sound.getFormat());
        Clip clip = null;
        try {clip = (Clip) AudioSystem.getLine(info);
        clip.open(sound); }
        catch(Exception e) {}

        label.setIcon(color);
        label.paintImmediately(0,0,50,50);

        LineListenerEnd listener = new LineListenerEnd();
        listener.finished = false;
        clip.addLineListener(listener);

        // play the sound clip
        clip.start();
        int i=0;
        while(listener.finished == false) {try {
                Thread.sleep(1);
            }catch(InterruptedException e) {
        } }
        label.setIcon(mono);
        label.paintImmediately(0,0,50,50);
    }

